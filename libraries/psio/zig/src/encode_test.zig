test "encode inventory API world matches golden binary" {
    const allocator = std.testing.allocator;
    const world = buildInventoryWorld();

    const result = try enc.encode(&world, allocator);
    defer allocator.free(result);

    // Verify length matches
    try std.testing.expectEqual(@as(usize, 708), result.len);
    try std.testing.expectEqual(@as(usize, 708), golden_binary.len);

    // Byte-for-byte comparison
    try std.testing.expectEqualSlices(u8, &golden_binary, result);
}

test "encode empty world produces header only" {
    const allocator = std.testing.allocator;
    const no_types = [_]WitTypeDef{};
    const no_funcs = [_]WitFunc{};
    const no_exports = [_]WitInterface{};

    const world = WitWorld{
        .package = "test:empty@1.0.0",
        .name = "empty-world",
        .types = &no_types,
        .funcs = &no_funcs,
        .exports = &no_exports,
    };

    const result = try enc.encode(&world, allocator);
    defer allocator.free(result);

    // Should have component header (8 bytes) + custom section only
    // No type section or export section since exports is empty
    try std.testing.expect(result.len > 8);

    // Check component magic
    try std.testing.expectEqualSlices(u8, &.{ 0x00, 0x61, 0x73, 0x6d, 0x0d, 0x00, 0x01, 0x00 }, result[0..8]);
}

test "primitive index round-trip" {
    inline for (0..13) |i| {
        const p: WitPrim = @enumFromInt(i);
        const idx = primIdx(p);
        try std.testing.expect(enc.isPrimIdx(idx));
        try std.testing.expectEqual(p, enc.idxToPrim(idx));
    }
}
