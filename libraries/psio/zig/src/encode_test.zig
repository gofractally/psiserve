// Golden binary comparison test for the WIT Component Model encoder.
//
// Builds the same InventoryApi world that the C++ tests use, encodes it,
// and compares byte-for-byte against the known-good binary output.

const std = @import("std");
const enc = @import("encode.zig");

const WitPrim = enc.WitPrim;
const WitTypeKind = enc.WitTypeKind;
const WitNamedType = enc.WitNamedType;
const WitTypeDef = enc.WitTypeDef;
const WitFunc = enc.WitFunc;
const WitInterface = enc.WitInterface;
const WitWorld = enc.WitWorld;
const primIdx = enc.primIdx;

// Primitive type index constants
const PrimBool = primIdx(.bool_);
const PrimU32 = primIdx(.u32_);
const PrimS32 = primIdx(.s32);
const PrimU64 = primIdx(.u64_);
const PrimString = primIdx(.string_);

fn buildInventoryWorld() WitWorld {
    // Type 0: record "item"
    const item_fields = [_]WitNamedType{
        .{ .name = "id", .type_idx = PrimU64 },
        .{ .name = "name", .type_idx = PrimString },
        .{ .name = "category", .type_idx = PrimString },
        .{ .name = "price-cents", .type_idx = PrimU32 },
        .{ .name = "in-stock", .type_idx = PrimBool },
        .{ .name = "tags", .type_idx = 1 }, // list<string>
        .{ .name = "weight-grams", .type_idx = 2 }, // option<u32>
    };

    // Type 1: list<string> (anonymous)
    // Type 2: option<u32> (anonymous)

    // Type 3: record "stock-result"
    const stock_result_fields = [_]WitNamedType{
        .{ .name = "item-id", .type_idx = PrimU32 },
        .{ .name = "old-quantity", .type_idx = PrimU32 },
        .{ .name = "new-quantity", .type_idx = PrimU32 },
    };

    // Type 4: record "search-query"
    const search_query_fields = [_]WitNamedType{
        .{ .name = "text", .type_idx = PrimString },
        .{ .name = "max-results", .type_idx = PrimU32 },
        .{ .name = "min-price", .type_idx = 2 }, // option<u32>
        .{ .name = "max-price", .type_idx = 2 }, // option<u32>
        .{ .name = "categories", .type_idx = 1 }, // list<string>
    };

    // Type 5: record "search-response"
    const search_response_fields = [_]WitNamedType{
        .{ .name = "items", .type_idx = 6 }, // list<item>
        .{ .name = "total-count", .type_idx = PrimU64 },
        .{ .name = "has-more", .type_idx = PrimBool },
    };

    // Type 6: list<item> (anonymous, element type index = 0)

    // Type 7: record "bulk-result"
    const bulk_result_fields = [_]WitNamedType{
        .{ .name = "inserted", .type_idx = PrimU32 },
        .{ .name = "failed", .type_idx = PrimU32 },
        .{ .name = "errors", .type_idx = 1 }, // list<string>
    };

    // Type 8: option<item> (anonymous, element type index = 0)

    const no_fields = [_]WitNamedType{};

    const type_defs = [_]WitTypeDef{
        // 0: record "item"
        .{ .name = "item", .kind = .record, .fields = &item_fields },
        // 1: list<string>
        .{ .name = "", .kind = .list, .fields = &no_fields, .element_type_idx = PrimString },
        // 2: option<u32>
        .{ .name = "", .kind = .option, .fields = &no_fields, .element_type_idx = PrimU32 },
        // 3: record "stock-result"
        .{ .name = "stock-result", .kind = .record, .fields = &stock_result_fields },
        // 4: record "search-query"
        .{ .name = "search-query", .kind = .record, .fields = &search_query_fields },
        // 5: record "search-response"
        .{ .name = "search-response", .kind = .record, .fields = &search_response_fields },
        // 6: list<item> (element = type 0)
        .{ .name = "", .kind = .list, .fields = &no_fields, .element_type_idx = 0 },
        // 7: record "bulk-result"
        .{ .name = "bulk-result", .kind = .record, .fields = &bulk_result_fields },
        // 8: option<item> (element = type 0)
        .{ .name = "", .kind = .option, .fields = &no_fields, .element_type_idx = 0 },
    };

    // Functions
    const get_item_params = [_]WitNamedType{.{ .name = "item-id", .type_idx = PrimU32 }};
    const get_item_results = [_]WitNamedType{.{ .name = "", .type_idx = 8 }};

    const list_items_params = [_]WitNamedType{.{ .name = "category", .type_idx = PrimString }};
    const list_items_results = [_]WitNamedType{.{ .name = "", .type_idx = 6 }};

    const add_item_params = [_]WitNamedType{.{ .name = "item", .type_idx = 0 }};
    const add_item_results = [_]WitNamedType{.{ .name = "", .type_idx = PrimU64 }};

    const update_stock_params = [_]WitNamedType{
        .{ .name = "item-id", .type_idx = PrimU32 },
        .{ .name = "delta", .type_idx = PrimS32 },
    };
    const update_stock_results = [_]WitNamedType{.{ .name = "", .type_idx = 3 }};

    const search_params = [_]WitNamedType{.{ .name = "query", .type_idx = 4 }};
    const search_results = [_]WitNamedType{.{ .name = "", .type_idx = 5 }};

    const bulk_import_params = [_]WitNamedType{.{ .name = "items", .type_idx = 6 }};
    const bulk_import_results = [_]WitNamedType{.{ .name = "", .type_idx = 7 }};

    const no_params = [_]WitNamedType{};
    const no_results = [_]WitNamedType{};

    const funcs = [_]WitFunc{
        // 0: get-item
        .{ .name = "get-item", .params = &get_item_params, .results = &get_item_results },
        // 1: list-items
        .{ .name = "list-items", .params = &list_items_params, .results = &list_items_results },
        // 2: add-item
        .{ .name = "add-item", .params = &add_item_params, .results = &add_item_results },
        // 3: update-stock
        .{ .name = "update-stock", .params = &update_stock_params, .results = &update_stock_results },
        // 4: search
        .{ .name = "search", .params = &search_params, .results = &search_results },
        // 5: bulk-import
        .{ .name = "bulk-import", .params = &bulk_import_params, .results = &bulk_import_results },
        // 6: ping
        .{ .name = "ping", .params = &no_params, .results = &no_results },
    };

    const type_idxs = [_]u32{ 0, 3, 4, 5, 7 };
    const func_idxs = [_]u32{ 0, 1, 2, 3, 4, 5, 6 };

    const exports = [_]WitInterface{
        .{
            .name = "inventory-api",
            .type_idxs = &type_idxs,
            .func_idxs = &func_idxs,
        },
    };

    return WitWorld{
        .package = "test:inventory@1.0.0",
        .name = "inventory-api-world",
        .types = &type_defs,
        .funcs = &funcs,
        .exports = &exports,
    };
}

